export type Language = keyof typeof translations;

const STORAGE_KEY = "astro-hack:ui-language";

const interpolate = (template: string, values?: Record<string, string | number>) => {
  if (!values) {
    return template;
  }

  return Object.entries(values).reduce(
    (output, [key, value]) => output.replaceAll(`{{${key}}}`, String(value)),
    template,
  );
};

const readStoredLanguage = (): Language => {
  if (typeof window === "undefined") {
    return "en";
  }

  try {
    const stored = window.localStorage.getItem(STORAGE_KEY);
    return stored === "tr" ? "tr" : "en";
  } catch {
    return "en";
  }
};

export const getTranslation = (
  language: Language,
  key: string,
  values?: Record<string, string | number>,
) => {
  const languageTable = translations[language] as Record<string, string>;
  const englishTable = translations.en as Record<string, string>;
  const template = languageTable[key] ?? englishTable[key] ?? key;
  return interpolate(template, values);
};

interface I18nContextValue {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (key: string, values?: Record<string, string | number>) => string;
}

const defaultContext: I18nContextValue = {
  language: "en",
  setLanguage: () => undefined,
  t: (key, values) => getTranslation("en", key, values),
};

const I18nContext = createContext<I18nContextValue>(defaultContext);

export const I18nProvider = ({ children }: { children: ReactNode }) => {
  const [language, setLanguage] = useState<Language>(readStoredLanguage);

  useEffect(() => {
    try {
      window.localStorage.setItem(STORAGE_KEY, language);
    } catch {
      // Ignore storage failures and keep the in-memory language state.
    }
  }, [language]);

  const value = useMemo<I18nContextValue>(
    () => ({
      language,
      setLanguage,
      t: (key, values) => getTranslation(language, key, values),
    }),
    [language],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export const useI18n = () => useContext(I18nContext);
